/*
Abstraction ==>  is the process of hiding the internal details of an object from the user and showing only the necessary information to the user.
Encapsulation ==> is the process of bundling data and methods together to form a single unit, and hiding the implementation details from the user.
Inheritance ==> is the process of creating a new class that inherits properties and methods from an existing class.
Polymorphism ==> is the ability of objects to take on multiple forms, depending on the context in which they are used.
*/

